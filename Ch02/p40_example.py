# (1)  동등 비교
num1, num2 = 3, 4
bool_result = num1 == num2
print(bool_result)
bool_result = num1 != num2
print(bool_result)

# (2) 크기 비교

bool_result = num1 > num2
print(bool_result)
bool_result = num1 >= num2
print(bool_result)
bool_result = num1 < num2
print(bool_result)
bool_result = num1 <= num2
print(bool_result)
